import Sim, { SimOptions } from '../../joist/js/Sim.js';
import simLauncher from '../../joist/js/simLauncher.js';
import Tandem from '../../tandem/js/Tandem.js';
import MagnetAndCompassScreen from './magnet-and-compass/MagnetAndCompassScreen.js';
import MagnetAndCompassStrings from './MagnetAndCompassStrings.js';
import './common/MagnetAndCompassQueryParameters.js';

// Launch the sim. Beware that scenery Image nodes created outside simLauncher.launch() will have zero bounds
// until the images are fully loaded. See https://github.com/phetsims/coulombs-law/issues/70#issuecomment-429037461
simLauncher.launch( () => {

  const titleStringProperty = MagnetAndCompassStrings[ 'magnet-and-compass' ].titleStringProperty;

  const screens = [
    new MagnetAndCompassScreen( { tandem: Tandem.ROOT.createTandem( 'magnetAndCompassScreen' ) } )
  ];

  const options: SimOptions = {

    //TODO fill in credits, all of these fields are optional, see joist.CreditsNode
    credits: {
      leadDesign: '',
      softwareDevelopment: '',
      team: '',
      contributors: '',
      qualityAssurance: '',
      graphicArts: '',
      soundDesign: '',
      thanks: ''
    }
  };

  const sim = new Sim( titleStringProperty, screens, options );
  sim.start();
} );